    /**
     * Максимальное независимое множество вершин в графе без циклов.
     * Сложная
     * <p>
     * Дан граф без циклов (получатель), например
     * <p>
     * G -- H -- J
     * |
     * A -- B -- D
     * |         |
     * C -- F    I
     * |
     * E
     * <p>
     * Найти в нём самое большое независимое множество вершин и вернуть его.
     * Никакая пара вершин в независимом множестве не должна быть связана ребром.
     * <p>
     * Если самых больших множеств несколько, приоритет имеет то из них,
     * в котором вершины расположены раньше во множестве this.vertices (начиная с первых).
     * <p>
     * В данном случае ответ (A, E, F, D, G, J)
     * <p>
     * Если на входе граф с циклами, бросить IllegalArgumentException
     */
    public static Set<Graph.Vertex> largestIndependentVertexSet(Graph graph) {
        //T = O(вершины + ребра)
        //R = O(вершины * вершины) храним множество, которое максимум вмещает в себя V множеств из V вершин

        Set<Graph.Vertex> vertices = graph.getVertices();
        if (vertices.isEmpty()) {
            return Collections.emptySet();
        }
        if (new Path().isLoop(graph)) throw new IllegalArgumentException();

        Set<Set<Graph.Vertex>> allSets = new HashSet<>();
        for (Graph.Vertex v : vertices) {
            Set<Graph.Vertex> set = new HashSet<>();
            Set<Graph.Vertex> connected = new HashSet<>();
            vertices.stream().filter(n -> !graph.getNeighbors(v).contains(n) && !connected.contains(n))
                    .forEach(n -> {
                        set.add(n);
                        connected.addAll(graph.getNeighbors(n));
                    });
            allSets.add(set);
        }

        int max = 0;
        Set<Graph.Vertex> maxSet = Collections.emptySet();
        for (Set<Graph.Vertex> s : allSets) {
            if (s.size() > max) {
                max = s.size();
                maxSet = s;
            }
        }

        if (max != 0) {
            return maxSet;
        } else return Collections.emptySet();
    }

    /**
     * Наидлиннейший простой путь.
     * Сложная
     * <p>
     * Дан граф (получатель). Найти в нём простой путь, включающий максимальное количество рёбер.
     * Простым считается путь, вершины в котором не повторяются.
     * Если таких путей несколько, вернуть любой из них.
     * <p>
     * Пример:
     * <p>
     * G -- H
     * |    |
     * A -- B -- C -- D
     * |    |    |    |
     * E    F -- I    |
     * |              |
     * J ------------ K
     * <p>
     * Ответ: A, E, J, K, D, C, H, G, B, F, I
     */
    public static Path longestSimplePath(Graph graph) {
        //T = О(V * V^V) для каждой вершины ищем простые пути в каждую из вершин
        //R = O(вершины * ребра)

        Path longest = new Path();
        Stack<Path> paths = new Stack<>();
        Set<Graph.Vertex> vertices = graph.getVertices();
        if (vertices.isEmpty()) return longest;
        for (Graph.Vertex v : vertices) paths.push(new Path(v));

        while (!paths.isEmpty()) {
            Path path = paths.pop();
            if (path.compareTo(longest) > 0) longest = path;
            graph.getNeighbors(path.getVertices().get(path.getLength())).stream()
                    .filter(n -> !path.contains(n)).forEach(n -> paths.push(new Path(path, graph, n)));
        }
        return longest;
    }


